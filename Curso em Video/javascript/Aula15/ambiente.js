let num = [5, 8, 2, 9, 3]

console.log(`Nosso vetor é o ${num}`) //mostra os elementos do vetor
console.log(`O vetor possui ${num.length} elementos`) //Mostra a quantidade de elementos do vetor
console.log(`O primeiro elemento do vetor é ${num[0]}`)
num.sort() //Ordena os elementos do vetor
console.log(`O nosso vetor ordenado fica ${num}`)
num.push(4) //Insere o valor do parênteses no final do vetor
console.log(`Vetor com inserção de valor no final ${num}`)